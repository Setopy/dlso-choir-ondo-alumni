// Helper functions
export function getAlumniByLocation(location: string): AlumniContactData[] {
  return alumniContacts.filter(alumni => 
    alumni.location?.toLowerCase().includes(location.toLowerCase())
  )
}

export function getAlumniByCountry(country: string): AlumniContactData[] {
  return alumniContacts.filter(alumni => 
    alumni.country.toLowerCase() === country.toLowerCase()
  )
}

export function searchAlumni(query: string): AlumniContactData[] {
  const searchTerm = query.toLowerCase()
  return alumniContacts.filter(alumni =>
    alumni.name.toLowerCase().includes(searchTerm) ||
    alumni.location?.toLowerCase().includes(searchTerm) ||
    alumni.ministrySection?.toLowerCase().includes(searchTerm)
  )
}

export function getAlumniStats() {
  const total = alumniContacts.length
  const countries = [...new Set(alumniContacts.map(a => a.country))]
  const locations = [...new Set(alumniContacts.filter(a => a.location).map(a => a.location!))]
  const withTitles = alumniContacts.filter(a => a.title).length
  const ondoAlumni = getAlumniByLocation('ondo').length
  const phdAlumni = getAlumniByLocation('phc').length
  
  return {
    total,
    countries: countries.length,
    locations: locations.length,
    withTitles,
    ondoAlumni,
    phdAlumni,
    countryBreakdown: countries.map(country => ({
      country,
      count: getAlumniByCountry(country).length
    }))
  }
}